
#1. Evaluating Student Performance

percentage = float(input("Enter the percentage score: "))

if percentage >= 90:
    print("Excellent performance") # Jika nilai persentase >= 90, maka tampilkan "Exellent performance"
elif percentage >= 80:
    print("Very Good performance") # Jika nilai persentase >= 80, maka tampilkan "Very Good performance"
elif percentage >= 70:
    print("Good performance") # Jika nilai persantase >= 70, maka tampilkan "Good performance"
elif percentage >= 60:
    print("Average performance") #Jika nilai persantase >= 60, Maka tampilkan "Average performance"
else:
    print("Below average performance") #Jika tidak Maka tamoilkan "Below avarage performance"